// Primitive Data Type 정수형에 대한 예제
public class DataType2 {
	public static void main(String[] args) {
		// 변수 선언 및 초기화
		long num1 = 1000L;	// long literal
		int num2 = 5;

		
		System.out.printf("num1: %d, num2: %d%nnum1+num2: %d%n", num1, num2, num1+num2);	// %d: 10진수, %n: 줄바꿈.


		System.out.println("num1: "+ num1);
	}
}