public class IfTest9{

	// main method
	public static void main(String[] args) {
		
		// initialize variable
		int num = 0;

		// 양수 음수 체크.

		if(num > 0) {
			System.out.println("양수입니다.");
		} else if (num < 0) {
			System.out.println("음수입니다.");
		} else {
			System.out.println("0입니다.");
		}

	}
}
