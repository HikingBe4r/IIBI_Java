public class ForForTest1{
	// Global variable

	// main method
	public static void main(String[] args) {
		
		// Local variable
		
		for(int i = 0; i < 3; i++) {
			for (int j = 0; j < 3; j++) {
				System.out.printf("i: %d, j: %d%n", i, j);
			}
			System.out.println("--------------");
		}
	}
}
